print('CALCULATOR')
function = input('Select the Function which you wanna perform + , - , * , / ')
No1=int(input('Enter 1st Number: '))
No2=int(input('Enter 2nd Number: '))
if function == '+':
    {
        print (No1+No2)
    }
elif function == '-':
    {
        print (No1-No2)
    }
elif function == '*':
    {
        print (No1*No2)
    }
elif function == '/':
    {
        print (No1/No2)
    } 